"""HotSpot floorplan (.flp) and layer-config (.lcf) generators."""
from __future__ import annotations

import os
from typing import List

from thermal.substrate import Bank, build_substrate, die_size_m, stack_height_m
from utils.config import (
    N_TIERS,
    SILICON_CP,
    SILICON_R,
    SILICON_THICKNESS_M,
    TIM_CP,
    TIM_R,
    TIM_THICKNESS_M,
)


def write_silicon_flp(banks_in_tier: List[Bank], path: str) -> None:
    """Write a HotSpot block-format floorplan for one silicon tier."""
    with open(path, "w") as f:
        f.write("# Auto-generated by gap_validation/thermal/floorplan_gen.py\n")
        f.write("# Format: <name> <width_m> <height_m> <x_m> <y_m>\n")
        for b in banks_in_tier:
            f.write(f"{b.name}\t{b.w_m:.6f}\t{b.h_m:.6f}\t{b.x_m:.6f}\t{b.y_m:.6f}\n")


def write_tim_flp(path: str, tier_idx: int, dx: float, dy: float) -> None:
    """Write a single-block TIM floorplan that spans the full die area."""
    with open(path, "w") as f:
        f.write("# Auto-generated TIM floorplan (single block, full die)\n")
        f.write(f"TIM_{tier_idx}\t{dx:.6f}\t{dy:.6f}\t0.000000\t0.000000\n")


def emit_layer_config(out_dir: str) -> str:
    """Emit floorplans + .lcf for the 4-tier stack with TIM interlayers.

    Layer ordering (bottom -> top):
      Layer 0 (silicon, tier 0)
      Layer 1 (TIM)
      Layer 2 (silicon, tier 1)
      Layer 3 (TIM)
      Layer 4 (silicon, tier 2)
      Layer 5 (TIM)
      Layer 6 (silicon, tier 3)
      Layer 7 (TIM cap)

    Returns the absolute path to the .lcf file.
    """
    os.makedirs(out_dir, exist_ok=True)
    fp_dir = os.path.join(out_dir, "floorplans")
    os.makedirs(fp_dir, exist_ok=True)

    banks = build_substrate()
    dx, dy = die_size_m()
    layers = []

    for t in range(N_TIERS):
        flp_path = os.path.join(fp_dir, f"silicon_tier{t}.flp")
        tier_banks = [b for b in banks if b.tier == t]
        write_silicon_flp(tier_banks, flp_path)
        layers.append(("silicon", flp_path, t))
        # TIM layer above (including cap)
        tim_path = os.path.join(fp_dir, f"tim_above_tier{t}.flp")
        write_tim_flp(tim_path, t, dx, dy)
        layers.append(("tim", tim_path, t))

    lcf_path = os.path.join(out_dir, "stack.lcf")
    with open(lcf_path, "w") as f:
        f.write("# Auto-generated 3D-NMP stack layer-config file\n")
        f.write("# Format: <layer#>, <lateralY/N>, <powerY/N>, <Cp>, <R>, <thickness_m>, <flp>\n\n")
        for idx, (kind, path, _) in enumerate(layers):
            if kind == "silicon":
                cp, r, th = SILICON_CP, SILICON_R, SILICON_THICKNESS_M
                power = "Y"
            else:
                cp, r, th = TIM_CP, TIM_R, TIM_THICKNESS_M
                power = "N"
            rel = os.path.relpath(path, out_dir)
            f.write(f"{idx}\nY\n{power}\n{cp}\n{r}\n{th}\n{rel}\n\n")
    return lcf_path


def stack_summary() -> dict:
    dx, dy = die_size_m()
    return {
        "die_size_m": [dx, dy],
        "n_tiers": N_TIERS,
        "stack_height_m": stack_height_m(),
        "silicon_thickness_m": SILICON_THICKNESS_M,
        "tim_thickness_m": TIM_THICKNESS_M,
    }
